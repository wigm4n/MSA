drop table users cascade ;
drop table tasks cascade ;
drop table messages cascade ;
drop table sessions cascade ;
drop table groups cascade ;
drop table norm cascade ;
drop table student cascade ;
drop table fisher cascade ;

create table users (
  id                serial not null primary key,
  email             varchar(64),
  firstname         varchar(255),
  lastname          varchar(255),
  patronymic        varchar(255),
  password          text
);

create table groups (
  id                serial not null primary key,
  creator_user_id   integer references users(id),
  name              varchar(255)
);

create table tasks (
  id                serial not null primary key,
  creator_user_id   integer references users(id),
  name              varchar(255),
  group_id          integer references groups(id),
  option_count      int,
  path_to_archive   text,
  path_to_tasks     text,
  date              timestamp
);

create table messages (
  id                serial not null primary key,
  task_id           integer references tasks(id),
  user_id           varchar(255),
  text              text,
  date              timestamp
);

create table sessions (
  id                serial not null primary key,
  user_id           integer references users(id),
  token             varchar(255)
);

create table norm (
  id                  serial not null primary key,
  alpha               double precision,
  val_alpha           double precision,
  semi_val_alpha      double precision
);

create table student (
  id                  serial not null primary key,
  alpha               double precision,
  freedom_degree      integer,
  val_alpha           double precision,
  semi_val_alpha      double precision
);

create table fisher (
  id                   serial not null primary key,
  alpha                double precision,
  freedom_degreeBig    integer,
  freedom_degreeLittle integer,
  val_alpha            double precision
);

